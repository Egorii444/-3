<?php
// Определяем массив с квадратами чисел от 1 до 10
$squares = array();
for ($i = 1; $i <= 10; $i++) {
    $squares[$i] = $i * $i;
}

// Выводим массив, указывая ключ и значение
echo "Массив квадратов чисел от 1 до 10:\n";
foreach ($squares as $key => $value) {
    echo "Ключ: $key, Значение: $value\n";
}
?>